<p>
	exclamo helps students who are victims of bullying to talk about their problems and solve them.
</p>
<p>	
	Students at an exclamo school receive an account for the app exclamo, through which they can anonymously contact their teachers and external bullying experts. In addition, there are helpful tips that help students deal with bullying and help them cope with incidents. Thus exclamo combines a low inhibition threshold with a simple solution of incidents by teachers who can take action directly at school.
</p>
<p>
	With the format of an app and web app, exclamo is in the medium that most benefits students - the smartphone - and provides native use for the person involved.
</p>