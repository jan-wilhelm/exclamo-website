<p>
	exclamo hilft Schülern, die Opfer von Mobbing sind, über ihre Probleme zu sprechen und sie zu lösen.
</p>
<p>
	Schüler einer exclamo-Schule bekommen einen Account für die App exclamo, über die sie sich auch anonym an ihre Lehrer und externe Mobbing-Experten wenden können. Zudem gibt es Tipps für den Umgang mit Mobbing, die den Schüler bei Vorfällen sofort unterstützen. Damit verbindet exclamo auf einzigartige Weise eine niedrige Hemmschwelle mit einer direkten Lösungsmöglichkeit durch Lehrer, die direkt in der Schule Maßnahmen ergreifen können.
</p>
<p>
	Mit dem Format einer App und Web-App ist exclamo in dem Medium, das Schüler am meisten nutzen – dem Smartphone - und bietet so für den Betroffenen eine native Handhabung.
</p>